package br.usjt.web.whisper.model;

public class CurtidaPost {
	
	private Usuario usuario;
	private Post post;
	
	public CurtidaPost() {
		
	}

	public Usuario getUsuario() {
		return usuario;
	}

	public void setUsuario(Usuario usuario) {
		this.usuario = usuario;
	}

	public Post getPost() {
		return post;
	}

	public void setPost(Post post) {
		this.post = post;
	}
	
}
